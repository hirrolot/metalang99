#include <epilepsy/assert.h>
#include <epilepsy/lang.h>
#include <epilepsy/uint.h>

// EPILEPSY_UINT_INC

EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_INC(v(0)), v(1));
EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_INC(v(15)), v(16));
EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_INC(v(198)), v(199));
EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_INC(v(1022)), v(1023));

// EPILEPSY_UINT_DEC

EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_DEC(v(1)), v(0));
EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_DEC(v(71)), v(70));
EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_DEC(v(918)), v(917));
EPILEPSY_ASSERT_EQ(EPILEPSY_UINT_DEC(v(1023)), v(1022));

// EPILEPSY_UINT_EQ

EPILEPSY_ASSERT(EPILEPSY_UINT_EQ(v(0), v(0)));
EPILEPSY_ASSERT(EPILEPSY_UINT_EQ(v(18), v(18)));
EPILEPSY_ASSERT(EPILEPSY_UINT_EQ(v(910), v(910)));
EPILEPSY_ASSERT(EPILEPSY_UINT_EQ(v(1023), v(1023)));

// EPILEPSY_UINT_NEQ

EPILEPSY_ASSERT(EPILEPSY_UINT_NEQ(v(0), v(1)));
EPILEPSY_ASSERT(EPILEPSY_UINT_NEQ(v(0), v(1023)));
EPILEPSY_ASSERT(EPILEPSY_UINT_NEQ(v(1), v(34)));
EPILEPSY_ASSERT(EPILEPSY_UINT_NEQ(v(184), v(1019)));
EPILEPSY_ASSERT(EPILEPSY_UINT_NEQ(v(603), v(16)));

// EPILEPSY_UINT_GREATER

EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER(v(1), v(0)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER(v(1023), v(0)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER(v(5), v(4)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER(v(147), v(80)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER(v(619), v(401)));

// EPILEPSY_UINT_LESSER

EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER(v(0), v(1)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER(v(0), v(1023)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER(v(19), v(25)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER(v(109), v(110)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER(v(817), v(1003)));

// EPILEPSY_UINT_GREATER_EQUAL

EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(0), v(0)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(18), v(18)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(175), v(175)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(1023), v(1023)));

EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(1), v(0)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(1023), v(0)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(19), v(10)));
EPILEPSY_ASSERT(EPILEPSY_UINT_GREATER_EQUAL(v(760), v(508)));

// EPILEPSY_UINT_LESSER_EQUAL

EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(0), v(0)));

EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(2), v(2)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(1), v(1)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(25), v(25)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(498), v(498)));

EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(0), v(1)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(0), v(1023)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(18), v(27)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(82), v(90)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(345), v(346)));
EPILEPSY_ASSERT(EPILEPSY_UINT_LESSER_EQUAL(v(901), v(1023)));
