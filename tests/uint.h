#include "test.h"

#include "../include/koshmar_pp/uint.h"

// KOSHMAR_PP_UINT_INC

TEST(KOSHMAR_PP_UINT_INC(0) == 1);
TEST(KOSHMAR_PP_UINT_INC(15) == 16);
TEST(KOSHMAR_PP_UINT_INC(198) == 199);
TEST(KOSHMAR_PP_UINT_INC(1022) == 1023);

// KOSHMAR_PP_UINT_DEC

TEST(KOSHMAR_PP_UINT_DEC(1) == 0);
TEST(KOSHMAR_PP_UINT_DEC(71) == 70);
TEST(KOSHMAR_PP_UINT_DEC(918) == 917);
TEST(KOSHMAR_PP_UINT_DEC(1023) == 1022);

// KOSHMAR_PP_UINT_EQ

TEST(KOSHMAR_PP_UINT_EQ(0, 0));
TEST(KOSHMAR_PP_UINT_EQ(18, 18));
TEST(KOSHMAR_PP_UINT_EQ(910, 910));
TEST(KOSHMAR_PP_UINT_EQ(1023, 1023));

// KOSHMAR_PP_UINT_NEQ

TEST(KOSHMAR_PP_UINT_NEQ(184, 1019));
TEST(KOSHMAR_PP_UINT_NEQ(109, 10));
TEST(KOSHMAR_PP_UINT_NEQ(197, 63));

TEST(!KOSHMAR_PP_UINT_NEQ(0, 0));
TEST(!KOSHMAR_PP_UINT_NEQ(184, 184));
TEST(!KOSHMAR_PP_UINT_NEQ(291, 291));

// KOSHMAR_PP_UINT_GREATER

TEST(!KOSHMAR_PP_UINT_GREATER(0, 0));
TEST(!KOSHMAR_PP_UINT_GREATER(0, 839));

TEST(!KOSHMAR_PP_UINT_GREATER(19, 19));
TEST(!KOSHMAR_PP_UINT_GREATER(933, 933));
TEST(!KOSHMAR_PP_UINT_GREATER(148, 148));
TEST(!KOSHMAR_PP_UINT_GREATER(793, 793));

TEST(KOSHMAR_PP_UINT_GREATER(5, 4));
TEST(KOSHMAR_PP_UINT_GREATER(1, 0));
TEST(KOSHMAR_PP_UINT_GREATER(147, 80));
TEST(KOSHMAR_PP_UINT_GREATER(619, 401));

// KOSHMAR_PP_UINT_LESSER

TEST(!KOSHMAR_PP_UINT_LESSER(0, 0));
TEST(!KOSHMAR_PP_UINT_LESSER(174, 19));

TEST(!KOSHMAR_PP_UINT_LESSER(11, 11));
TEST(!KOSHMAR_PP_UINT_LESSER(6, 6));
TEST(!KOSHMAR_PP_UINT_LESSER(194, 194));
TEST(!KOSHMAR_PP_UINT_LESSER(971, 971));

TEST(KOSHMAR_PP_UINT_LESSER(7, 8));
TEST(KOSHMAR_PP_UINT_LESSER(19, 256));
TEST(KOSHMAR_PP_UINT_LESSER(918, 1023));
TEST(KOSHMAR_PP_UINT_LESSER(817, 1003));

// KOSHMAR_PP_UINT_GREATER_EQUAL

TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(0, 0));
TEST(!KOSHMAR_PP_UINT_GREATER_EQUAL(0, 15));

TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(18, 18));
TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(191, 191));
TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(175, 175));
TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(867, 867));

TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(187, 19));
TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(19, 10));
TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(199, 198));
TEST(KOSHMAR_PP_UINT_GREATER_EQUAL(760, 508));

// KOSHMAR_PP_UINT_LESSER_EQUAL

TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(0, 0));
TEST(!KOSHMAR_PP_UINT_LESSER_EQUAL(180, 18));

TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(2, 2));
TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(1, 1));
TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(25, 25));
TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(498, 498));

TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(18, 27));
TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(82, 90));
TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(345, 346));
TEST(KOSHMAR_PP_UINT_LESSER_EQUAL(901, 1008));
