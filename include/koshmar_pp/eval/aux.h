#ifndef KOSHMAR_PP_PRIVATE_AUX_H
#define KOSHMAR_PP_PRIVATE_AUX_H

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_EMPTY()
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_EXPAND(...)         __VA_ARGS__
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_COMMA()             ,
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_CALL_MACRO(op, ...) op(__VA_ARGS__)

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_PARENTHESISE(...) (__VA_ARGS__)
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_UNPARENTHESISE(x)                                              \
    KOSHMAR_PP_PRIVATE_EVAL_AUX_EXPAND(KOSHMAR_PP_PRIVATE_EVAL_AUX_EXPAND x)

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_CAT(x, y)           KOSHMAR_PP_PRIVATE_EVAL_AUX_PRIMITIVE_CAT(x, y)
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_PRIMITIVE_CAT(x, y) x##y

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_MATCH(op, pattern) KOSHMAR_PP_PRIVATE_EVAL_AUX_CAT(op, pattern)

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_HEAD(...)        KOSHMAR_PP_PRIVATE_EVAL_AUX_HEAD_AUX(__VA_ARGS__)
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_HEAD_AUX(x, ...) x

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_TAIL(...)        KOSHMAR_PP_PRIVATE_EVAL_AUX_TAIL_AUX(__VA_ARGS__)
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_TAIL_AUX(x, ...) __VA_ARGS__

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_EMPTY_ACC() (KOSHMAR_PP_PRIVATE_EVAL_AUX_EMPTY())
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_EXTEND_ACC(acc, ...)                                           \
    (KOSHMAR_PP_PRIVATE_EVAL_AUX_UNPARENTHESISE(acc) __VA_ARGS__)

#define KOSHMAR_PP_PRIVATE_EVAL_AUX_IF(cond, x, y)                                                 \
    KOSHMAR_PP_PRIVATE_EVAL_AUX_MATCH(KOSHMAR_PP_PRIVATE_EVAL_AUX_IF_, cond)((x), (y))
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_IF_0(_x, y) KOSHMAR_PP_PRIVATE_EVAL_AUX_UNPARENTHESISE(y)
#define KOSHMAR_PP_PRIVATE_EVAL_AUX_IF_1(x, _y) KOSHMAR_PP_PRIVATE_EVAL_AUX_UNPARENTHESISE(x)

#endif // KOSHMAR_PP_PRIVATE_AUX_H
