#ifndef INSULT_PRIVATE_AUX_H
#define INSULT_PRIVATE_AUX_H

#define INSULT_PRIVATE_EVAL_AUX_EMPTY()
#define INSULT_PRIVATE_EVAL_AUX_EXPAND(...)         __VA_ARGS__
#define INSULT_PRIVATE_EVAL_AUX_COMMA()             ,
#define INSULT_PRIVATE_EVAL_AUX_CALL_MACRO(op, ...) op(__VA_ARGS__)

#define INSULT_PRIVATE_EVAL_AUX_PARENTHESISE(...) (__VA_ARGS__)
#define INSULT_PRIVATE_EVAL_AUX_UNPARENTHESISE(x)                                                  \
    INSULT_PRIVATE_EVAL_AUX_EXPAND(INSULT_PRIVATE_EVAL_AUX_EXPAND x)

#define INSULT_PRIVATE_EVAL_AUX_CAT(x, y)           INSULT_PRIVATE_EVAL_AUX_PRIMITIVE_CAT(x, y)
#define INSULT_PRIVATE_EVAL_AUX_PRIMITIVE_CAT(x, y) x##y

#define INSULT_PRIVATE_EVAL_AUX_MATCH(op, pattern) INSULT_PRIVATE_EVAL_AUX_CAT(op, pattern)

#define INSULT_PRIVATE_EVAL_AUX_HEAD(...)        INSULT_PRIVATE_EVAL_AUX_HEAD_AUX(__VA_ARGS__)
#define INSULT_PRIVATE_EVAL_AUX_HEAD_AUX(x, ...) x

#define INSULT_PRIVATE_EVAL_AUX_TAIL(...)        INSULT_PRIVATE_EVAL_AUX_TAIL_AUX(__VA_ARGS__)
#define INSULT_PRIVATE_EVAL_AUX_TAIL_AUX(x, ...) __VA_ARGS__

#define INSULT_PRIVATE_EVAL_AUX_EMPTY_ACC() (INSULT_PRIVATE_EVAL_AUX_EMPTY())
#define INSULT_PRIVATE_EVAL_AUX_EXTEND_PARENTHESISED(acc, ...)                                     \
    (INSULT_PRIVATE_EVAL_AUX_UNPARENTHESISE(acc) __VA_ARGS__)

#define INSULT_PRIVATE_EVAL_AUX_IF(cond, x, y)                                                     \
    INSULT_PRIVATE_EVAL_AUX_MATCH(INSULT_PRIVATE_EVAL_AUX_IF_, cond)((x), (y))
#define INSULT_PRIVATE_EVAL_AUX_IF_0(_x, y) INSULT_PRIVATE_EVAL_AUX_UNPARENTHESISE(y)
#define INSULT_PRIVATE_EVAL_AUX_IF_1(x, _y) INSULT_PRIVATE_EVAL_AUX_UNPARENTHESISE(x)

#endif // INSULT_PRIVATE_AUX_H
